<?php

/**
 * The file that defines the core plugin class
 *
 * A class definition that includes attributes and functions used across both the
 * public-facing side of the site and the admin area.
 *
 * @link       https://mehrdaddindar.ir
 * @since      1.0.0
 *
 * @package    Vanak
 * @subpackage Vanak/includes
 */

/**
 * The core plugin class.
 *
 * This is used to define internationalization, admin-specific hooks, and
 * public-facing site hooks.
 *
 * Also maintains the unique identifier of this plugin as well as the current
 * version of the plugin.
 *
 * @since      1.0.0
 * @package    Vanak
 * @subpackage Vanak/includes
 * @author     Mehrdad Dindar <mehrdad.dindar@live.com>
 */
class Vanak {

	/**
	 * The loader that's responsible for maintaining and registering all hooks that power
	 * the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      Vanak_Loader    $loader    Maintains and registers all hooks for the plugin.
	 */
	protected $loader;

	/**
	 * The unique identifier of this plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      string    $plugin_name    The string used to uniquely identify this plugin.
	 */
	protected $plugin_name;

	/**
	 * The current version of the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      string    $version    The current version of the plugin.
	 */
	protected $version;

	/**
	 * Define the core functionality of the plugin.
	 *
	 * Set the plugin name and the plugin version that can be used throughout the plugin.
	 * Load the dependencies, define the locale, and set the hooks for the admin area and
	 * the public-facing side of the site.
	 *
	 * @since    1.0.0
	 */
	public function __construct() {
		if ( defined( 'VANAK_VERSION' ) ) {
			$this->version = VANAK_VERSION;
		} else {
			$this->version = '1.0.0';
		}
		$this->plugin_name = 'vanak';

		$this->load_dependencies();
		$this->set_locale();
		$this->define_admin_hooks();
		$this->define_public_hooks();

	}

	/**
	 * Load the required dependencies for this plugin.
	 *
	 * Include the following files that make up the plugin:
	 *
	 * - Vanak_Loader. Orchestrates the hooks of the plugin.
	 * - Vanak_i18n. Defines internationalization functionality.
	 * - Vanak_Admin. Defines all hooks for the admin area.
	 * - Vanak_Public. Defines all hooks for the public side of the site.
	 *
	 * Create an instance of the loader which will be used to register the hooks
	 * with WordPress.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function load_dependencies() {

        /**
         * Require autoload to project for everything
         */
        $vendor_autoload_path = trailingslashit(dirname(VANAK_PATH)) . 'vendor/autoload.php';
        if (file_exists($vendor_autoload_path)) {
            require_once $vendor_autoload_path;
        }
        /**
         * Require JDF to project for jalali date format
         */
        require_once(dirname(__FILE__) . '/jdf.php');

        /**
         * Include BaleApiLibrary to project
         */
		include dirname(__FILE__) . "/BaleAPIv2.php";

		/**
         * Require Nuxy to project for managing setup page
         */
        require_once(dirname(__FILE__) . '/nuxy/NUXY.php');

		/**
         * Require Nuxy dependencies to project
         */
        require_once(dirname(__FILE__) . '/nuxy/nuxy_dependence.php');

		/**
		 * The class responsible for orchestrating the actions and filters of the
		 * core plugin.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-vanak-loader.php';

		/**
		 * The class responsible for defining internationalization functionality
		 * of the plugin.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-vanak-i18n.php';

		/**
		 * The class responsible for defining all actions that occur in the admin area.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'admin/class-vanak-admin.php';

		/**
		 * The class responsible for defining all actions that occur in the public-facing
		 * side of the site.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'public/class-vanak-public.php';

		$this->loader = new Vanak_Loader();

	}

	/**
	 * Define the locale for this plugin for internationalization.
	 *
	 * Uses the Vanak_i18n class in order to set the domain and to register the hook
	 * with WordPress.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function set_locale() {

		$plugin_i18n = new Vanak_i18n();

		$this->loader->add_action( 'plugins_loaded', $plugin_i18n, 'load_plugin_textdomain' );

	}

	/**
	 * Register all of the hooks related to the admin area functionality
	 * of the plugin.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function define_admin_hooks() {

		$plugin_admin = new Vanak_Admin( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'admin_enqueue_scripts', $plugin_admin, 'enqueue_styles' );
		$this->loader->add_action( 'admin_enqueue_scripts', $plugin_admin, 'enqueue_scripts' );

		//	Action hook for check nuxy dependencies
		$this->loader->add_action('wp_ajax_wpcfto_save_settings', $plugin_admin, 'nuxyCheck');

		//	Action hook for admin menu
		$this->loader->add_filter('wpcfto_options_page_setup', $plugin_admin, 'options_page');

		//	Action hook for setWebhook
		$this->loader->add_action('wp_ajax_wpcfto_save_settings', $plugin_admin, 'welcome');

		//	Action hook for Admin Login
		$this->loader->add_action('wp_login', $plugin_admin, 'admin_login', 10, 2);
	}

	/**
	 * Register all of the hooks related to the public-facing functionality
	 * of the plugin.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function define_public_hooks() {

		$plugin_public = new Vanak_Public( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'wp_enqueue_scripts', $plugin_public, 'enqueue_styles' );
		$this->loader->add_action( 'wp_enqueue_scripts', $plugin_public, 'enqueue_scripts' );

		if ( !get_option('vanak_license')) {
			// action hook for unscheduled hook
			$this->loader->add_action('vanak_unscheduled_hook', $plugin_public,'unscheduled_vanak_task');
		} else {

            // action hook for ajax woocommerce_checkout_order_processed
            if ( get_option('vanak_settings')["order_submitted"] ?? false) {
                $this->loader->add_action('woocommerce_checkout_order_processed', $plugin_public, 'sendNewOrder', 10, 1);
            }

            // action hook for new comments posted
            if ( get_option('vanak_settings')["comment_submitted"] ?? false) {
                $this->loader->add_action('comment_post', $plugin_public,'sendNewComment', 10, 3);
            }

            // action hook for guard check
            $this->loader->add_action('vanak_guard_check_hook', $plugin_public, 'guard_check_function');
        }

	}

	/**
	 * Run the loader to execute all of the hooks with WordPress.
	 *
	 * @since    1.0.0
	 */
	public function run() {
		$this->loader->run();
	}

	/**
	 * The name of the plugin used to uniquely identify it within the context of
	 * WordPress and to define internationalization functionality.
	 *
	 * @since     1.0.0
	 * @return    string    The name of the plugin.
	 */
	public function get_plugin_name() {
		return $this->plugin_name;
	}

	/**
	 * The reference to the class that orchestrates the hooks with the plugin.
	 *
	 * @since     1.0.0
	 * @return    Vanak_Loader    Orchestrates the hooks of the plugin.
	 */
	public function get_loader() {
		return $this->loader;
	}

	/**
	 * Retrieve the version number of the plugin.
	 *
	 * @since     1.0.0
	 * @return    string    The version number of the plugin.
	 */
	public function get_version() {
		return $this->version;
	}

}
